import { useState, useEffect } from 'react';
import { useLocation } from 'react-router-dom';
import { publicAPI } from '../services/api';

interface EnvioInfo {
  id: number;
  numero_guia: string;
  estado: string;
  estado_display: string;
  remitente_nombre: string;
  remitente_telefono?: string;
  destinatario_nombre: string;
  destinatario_telefono?: string;
  fecha_creacion: string;
  fecha_actualizacion: string;
  descripcion?: string;
  peso?: number;
  valor_declarado?: number;
}

interface TrackingResponse {
  numero_guia: string;
  estado: string;
  estado_display: string;
  remitente_nombre: string;
  destinatario_nombre: string;
  fecha_actualizacion: string;
  historial: Array<{
    estado: string;
    fecha: string;
    comentario?: string;
    ubicacion?: string;
  }>;
}

const TrackingPageFixed = () => {
  const [busqueda, setBusqueda] = useState('');
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState('');
  const [envios, setEnvios] = useState<EnvioInfo[]>([]);
  const [tipoBusqueda, setTipoBusqueda] = useState<'guia' | 'remitente' | 'destinatario'>('guia');
  const location = useLocation();

  useEffect(() => {
    console.log('üîç TrackingPageFixed: Componente montado');
    console.log('üìç Ubicaci√≥n actual:', location.pathname);
    console.log('üåê URL completa:', window.location.href);
  }, [location]);

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    console.log('ÔøΩ TrackingPageFixed: handleSubmit iniciado');
    console.log('ÔøΩüîç TrackingPageFixed: Buscando:', { busqueda, tipoBusqueda });

    if (!busqueda.trim()) {
      console.log('‚ùå B√∫squeda vac√≠a');
      setError('Por favor ingresa un criterio de b√∫squeda');
      return;
    }

    console.log('‚è≥ Iniciando loading...');
    setLoading(true);
    setError('');
    setEnvios([]);

    console.log('üîÑ Estado actualizado - loading: true, error: "", envios: []');

    try {
      let response;
      let resultados: EnvioInfo[] = [];

      console.log('üîç Iniciando b√∫squeda:', tipoBusqueda, busqueda);

      switch (tipoBusqueda) {
        case 'guia':
          // Buscar por n√∫mero de gu√≠a (funci√≥n existente)
          console.log('üì° Llamando a publicAPI.trackPackage...');
          const startTime = performance.now();

          response = await publicAPI.trackPackage(busqueda);

          const endTime = performance.now();
          console.log(`‚è±Ô∏è Tiempo de respuesta: ${(endTime - startTime).toFixed(2)}ms`);
          console.log('üì¶ Respuesta trackPackage completa:', {
            status: response.status,
            data: response.data,
            error: response.error
          });

          if (response.status === 200 && response.data) {
            // Mapear la respuesta al formato esperado
            const envioData = response.data as TrackingResponse;
            const envioMapeado: EnvioInfo = {
              id: Date.now(), // ID temporal para React
              numero_guia: envioData.numero_guia,
              estado: envioData.estado,
              estado_display: envioData.estado_display,
              remitente_nombre: envioData.remitente_nombre,
              destinatario_nombre: envioData.destinatario_nombre,
              fecha_creacion: envioData.fecha_actualizacion, // Usar fecha_actualizacion como creaci√≥n
              fecha_actualizacion: envioData.fecha_actualizacion,
              descripcion: 'Informaci√≥n disponible via rastreo p√∫blico',
              peso: 0,
              valor_declarado: 0
            };
            resultados = [envioMapeado];
            console.log('‚úÖ Env√≠o mapeado correctamente:', envioMapeado);
          } else if (response.status === 404) {
            console.log('‚ö†Ô∏è Env√≠o no encontrado (404)');
            setError(`No se encontr√≥ ning√∫n env√≠o con la gu√≠a: "${busqueda}"`);
          } else if (response.error) {
            console.log('‚ö†Ô∏è Error en respuesta:', response.error);
            setError(response.error);
          } else {
            console.log('‚ö†Ô∏è Respuesta inesperada:', response);
            setError('Respuesta inesperada del servidor');
          }
          break;

        case 'remitente':
          // Buscar por nombre del remitente
          console.log('üë§ Llamando a publicAPI.searchByRemitente...');
          const startTimeRemitente = performance.now();

          response = await publicAPI.searchByRemitente(busqueda);

          const endTimeRemitente = performance.now();
          console.log(`‚è±Ô∏è Tiempo de respuesta remitente: ${(endTimeRemitente - startTimeRemitente).toFixed(2)}ms`);
          console.log('üë§ Respuesta searchByRemitente completa:', {
            status: response.status,
            data: response.data,
            error: response.error
          });

          if (response.status === 200 && response.data) {
            const searchData = response.data as { count: number; results: TrackingResponse[] };
            // Mapear m√∫ltiples resultados
            resultados = searchData.results.map((envioData, index) => ({
              id: Date.now() + index, // ID temporal √∫nico para React
              numero_guia: envioData.numero_guia,
              estado: envioData.estado,
              estado_display: envioData.estado_display,
              remitente_nombre: envioData.remitente_nombre,
              destinatario_nombre: envioData.destinatario_nombre,
              fecha_creacion: envioData.fecha_actualizacion,
              fecha_actualizacion: envioData.fecha_actualizacion,
              descripcion: 'Informaci√≥n disponible via b√∫squeda por remitente',
              peso: 0,
              valor_declarado: 0
            }));
            console.log(`‚úÖ ${resultados.length} env√≠o(s) mapeados correctamente por remitente`);
          } else if (response.status === 404) {
            console.log('‚ö†Ô∏è No se encontraron env√≠os para el remitente (404)');
            setError(`No se encontraron env√≠os para el remitente: "${busqueda}"`);
          } else if (response.error) {
            console.log('‚ö†Ô∏è Error en respuesta de remitente:', response.error);
            setError(response.error);
          } else {
            console.log('‚ö†Ô∏è Respuesta inesperada de remitente:', response);
            setError('Respuesta inesperada del servidor');
          }
          break;

        case 'destinatario':
          // Buscar por nombre del destinatario
          console.log('üìç Llamando a publicAPI.searchByDestinatario...');
          const startTimeDestinatario = performance.now();

          response = await publicAPI.searchByDestinatario(busqueda);

          const endTimeDestinatario = performance.now();
          console.log(`‚è±Ô∏è Tiempo de respuesta destinatario: ${(endTimeDestinatario - startTimeDestinatario).toFixed(2)}ms`);
          console.log('üìç Respuesta searchByDestinatario completa:', {
            status: response.status,
            data: response.data,
            error: response.error
          });

          if (response.status === 200 && response.data) {
            const searchData = response.data as { count: number; results: TrackingResponse[] };
            // Mapear m√∫ltiples resultados
            resultados = searchData.results.map((envioData, index) => ({
              id: Date.now() + index, // ID temporal √∫nico para React
              numero_guia: envioData.numero_guia,
              estado: envioData.estado,
              estado_display: envioData.estado_display,
              remitente_nombre: envioData.remitente_nombre,
              destinatario_nombre: envioData.destinatario_nombre,
              fecha_creacion: envioData.fecha_actualizacion,
              fecha_actualizacion: envioData.fecha_actualizacion,
              descripcion: 'Informaci√≥n disponible via b√∫squeda por destinatario',
              peso: 0,
              valor_declarado: 0
            }));
            console.log(`‚úÖ ${resultados.length} env√≠o(s) mapeados correctamente por destinatario`);
          } else if (response.status === 404) {
            console.log('‚ö†Ô∏è No se encontraron env√≠os para el destinatario (404)');
            setError(`No se encontraron env√≠os para el destinatario: "${busqueda}"`);
          } else if (response.error) {
            console.log('‚ö†Ô∏è Error en respuesta de destinatario:', response.error);
            setError(response.error);
          } else {
            console.log('‚ö†Ô∏è Respuesta inesperada de destinatario:', response);
            setError('Respuesta inesperada del servidor');
          }
          break;
      }

      console.log('üìä Resultados obtenidos:', resultados);
      setEnvios(resultados);

      if (resultados.length === 0 && !error) {
        setError(`No se encontraron env√≠os para el ${tipoBusqueda}: "${busqueda}"`);
      } else {
        console.log(`‚úÖ Se encontraron ${resultados.length} env√≠o(s)`);
      }

    } catch (err: any) {
      console.error('üîç TrackingPageFixed: Error en b√∫squeda:', err);
      console.error('üîç Error completo:', {
        message: err.message,
        name: err.name,
        stack: err.stack,
        response: err.response,
        status: err.response?.status,
        data: err.response?.data
      });

      let errorMessage = 'No se puede conectar con el servidor';

      // Manejo mejorado de errores
      if (err.name === 'TypeError' && err.message.includes('fetch')) {
        errorMessage = 'Error de red. Verifica tu conexi√≥n a internet y que el servidor est√© funcionando.';
      } else if (err.response?.status === 404) {
        errorMessage = `No se encontr√≥ ning√∫n env√≠o con el ${tipoBusqueda}: "${busqueda}"`;
      } else if (err.response?.status === 401) {
        errorMessage = 'Error de autenticaci√≥n. Por favor inicia sesi√≥n nuevamente.';
      } else if (err.response?.status === 500) {
        errorMessage = 'Error del servidor. Por favor intenta nuevamente m√°s tarde.';
      } else if (err.response?.status === 0) {
        errorMessage = 'No se puede conectar con el servidor. Verifica que el servicio est√© funcionando.';
      } else if (err.response?.data?.error) {
        errorMessage = err.response.data.error;
      } else if (err.message) {
        errorMessage = `Error de conexi√≥n: ${err.message}`;
      }

      console.error('üì¢ Error final mostrado al usuario:', errorMessage);
      setError(errorMessage);
    } finally {
      console.log('üèÅ Finally ejecut√°ndose - loading: false');
      setLoading(false);
      console.log('‚úÖ handleSubmit terminado');
    }
  };

  const formatFecha = (fecha: string) => {
    return new Date(fecha).toLocaleDateString('es-ES', {
      year: 'numeric',
      month: 'long',
      day: 'numeric',
      hour: '2-digit',
      minute: '2-digit'
    });
  };

  console.log('üñºÔ∏è TrackingPageFixed renderizando:', {
    loading,
    error,
    enviosCount: envios.length,
    busqueda,
    tipoBusqueda
  });

  return (
    <div className="tracking-page">
      <div className="container">
        <h1>üîç Rastrear Env√≠os - ¬°ACTUALIZADO! üöÄ</h1>
        <p>Busca env√≠os por n√∫mero de gu√≠a, nombre del remitente o destinatario</p>

        <form onSubmit={handleSubmit} className="tracking-form">
          <div className="form-group">
            <label htmlFor="tipoBusqueda">Tipo de b√∫squeda:</label>
            <select
              id="tipoBusqueda"
              value={tipoBusqueda}
              onChange={(e) => setTipoBusqueda(e.target.value as 'guia' | 'remitente' | 'destinatario')}
              className="form-control"
            >
              <option value="guia">N√∫mero de Gu√≠a</option>
              <option value="remitente">Nombre del Remitente</option>
              <option value="destinatario">Nombre del Destinatario</option>
            </select>
          </div>

          <div className="form-group">
            <input
              type="text"
              value={busqueda}
              onChange={(e) => setBusqueda(e.target.value)}
              placeholder={
                tipoBusqueda === 'guia' ? 'Ej: PKF00000001' :
                tipoBusqueda === 'remitente' ? 'Ej: Juan P√©rez' :
                'Ej: Mar√≠a Garc√≠a'
              }
              disabled={loading}
              className="form-control"
            />
            <button
              type="submit"
              disabled={loading || !busqueda.trim()}
              className="btn btn-primary"
            >
              {loading ? 'Buscando...' : 'Buscar Env√≠os'}
            </button>
          </div>
        </form>

        {error && (
          <div className="alert alert-warning">
            {error}
          </div>
        )}

        {envios.length > 0 && (
          <div className="resultados-busqueda">
            <h2>ÔøΩ Env√≠os Encontrados ({envios.length})</h2>

            {envios.map((envio) => (
              <div key={envio.id} className="envio-card">
                <div className="envio-header">
                  <h3>Gu√≠a: {envio.numero_guia}</h3>
                  <span
                    className={`estado-badge estado-${envio.estado.toLowerCase()}`}
                  >
                    {envio.estado_display}
                  </span>
                </div>

                <div className="envio-details">
                  <div className="detail-row">
                    <strong>ÔøΩ Remitente:</strong> {envio.remitente_nombre}
                    {envio.remitente_telefono && <span> - üìû {envio.remitente_telefono}</span>}
                  </div>

                  <div className="detail-row">
                    <strong>üìç Destinatario:</strong> {envio.destinatario_nombre}
                    {envio.destinatario_telefono && <span> - üìû {envio.destinatario_telefono}</span>}
                  </div>

                  {envio.descripcion && (
                    <div className="detail-row">
                      <strong>üìã Descripci√≥n:</strong> {envio.descripcion}
                    </div>
                  )}

                  {envio.peso && (
                    <div className="detail-row">
                      <strong>‚öñÔ∏è Peso:</strong> {envio.peso} kg
                    </div>
                  )}

                  <div className="detail-row">
                    <strong>üìÖ Creado:</strong> {formatFecha(envio.fecha_creacion)}
                  </div>

                  <div className="detail-row">
                    <strong>üîÑ √öltima actualizaci√≥n:</strong> {formatFecha(envio.fecha_actualizacion)}
                  </div>
                </div>
              </div>
            ))}
          </div>
        )}

        <div className="debug-panel">
          <h3>üîß Panel de Debug</h3>
          <div className="debug-info">
            <p><strong>üîç Componente:</strong> ‚úÖ TrackingPageFixed funcionando</p>
            <p><strong>ÔøΩ Ruta:</strong> {location.pathname}</p>
            <p><strong>ÔøΩ Tipo b√∫squeda:</strong> {tipoBusqueda}</p>
            <p><strong>üìù B√∫squeda:</strong> {busqueda || '‚ùå Vac√≠o'}</p>
            <p><strong>ÔøΩ Env√≠os encontrados:</strong> {envios.length}</p>
            <p><strong>üîÑ Estado:</strong> {loading ? 'üîÑ Cargando' : '‚úÖ Listo'}</p>
            <p><strong>‚ùó Error:</strong> {error || '‚úÖ Sin errores'}</p>
          </div>
        </div>
      </div>
    </div>
  );
};

export default TrackingPageFixed;
